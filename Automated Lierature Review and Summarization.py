def build_eval_table(results):
    rows = []
    for r in results:
        ex = r.get('eval_extractive', {})
        ab = r.get('eval_abstractive', {})
        rows.append({
            "paper_id": r['paper_id'],
            "title": r['title'][:80],
            "rouge1_ext": ex.get('rouge1', None),
            "rouge2_ext": ex.get('rouge2', None),
            "rougeL_ext": ex.get('rougeL', None),
            "bertF1_ext": ex.get('bertscore_f1', None),
            "rouge1_abs": ab.get('rouge1', None),
            "rouge2_abs": ab.get('rouge2', None),
            "rougeL_abs": ab.get('rougeL', None),
            "bertF1_abs": ab.get('bertscore_f1', None),
        })
    return pd.DataFrame(rows)


_state_cache = {}  # cache state per (query,max_results)


def gradio_search_and_run(query, method, top_k, max_results):
    try:
        key = f"{query}||{int(max_results)}"
        if key not in _state_cache:
            state = run_pipeline_for_query(query, max_results=int(max_results))
            _state_cache[key] = state
        else:
            state = _state_cache[key]

        output = pipeline_query_demo_from_state(
            state, query, method=method, top_k=int(top_k)
        )

        ir_scores = output["ir_scores"]
        results = output["results"]

    except Exception as e:
        return f"Error: {e}", pd.DataFrame(), plt.figure()

    # IR Metrics
    ir_text = (
        "=== INFORMATION RETRIEVAL ACCURACY ===\n"
        f"Precision: {ir_scores['precision']:.3f}\n"
        f"Recall:    {ir_scores['recall']:.3f}\n"
        f"F1-score:  {ir_scores['f1']:.3f}\n"
        f"Relevant papers found: {ir_scores['relevant_found']} / {ir_scores['total_relevant']}\n"
        + "-"*70 + "\n\n"
    )

    display_text = ir_text

    # Summaries + scores
    for r in results:
        display_text += f"Paper: {r['paper_id']} (score={r['score']:.4f})\n"
        display_text += f"Title: {r['title']}\n\n"

        if r['reference_abstract']:
            display_text += "Reference abstract:\n" + r['reference_abstract'][:400] + "\n\n"

        display_text += "=== Extractive Summary ===\n" + r['extractive_summary'] + "\n\n"
        ex = r['eval_extractive']
        if ex:
            display_text += f"ROUGE1={ex.get('rouge1',0):.3f}, ROUGE2={ex.get('rouge2',0):.3f}, ROUGEL={ex.get('rougeL',0):.3f}, BERT={ex.get('bertscore_f1',0):.3f}\n\n"

        display_text += "=== Abstractive Summary ===\n" + r['abstractive_summary'] + "\n\n"
        ab = r['eval_abstractive']
        if ab:
            display_text += f"ROUGE1={ab.get('rouge1',0):.3f}, ROUGE2={ab.get('rouge2',0):.3f}, ROUGEL={ab.get('rougeL',0):.3f}, BERT={ab.get('bertscore_f1',0):.3f}\n\n"

        display_text += "-"*80 + "\n\n"

    df_eval = build_eval_table(results)

    # Build citation graph figure
    G = state["citation_graph"]
    pagerank = state["pagerank"]
    fig1 = visualize_citation_graph(G, pagerank)
    fig2 = plot_year_distribution(state["doc_ids"])
    return display_text, df_eval, fig1, fig2



