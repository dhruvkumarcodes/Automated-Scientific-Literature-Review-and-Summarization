SLEEP = 0.5  # polite API delay

def fetch_references(pid):
    """Fetch references from Semantic Scholar for a given arXiv ID."""
    paper_id = f"ARXIV:{pid}"
    fields = "title,externalIds,references.title,references.externalIds,citations.title,citations.externalIds"
    url = SEM_SCHOLAR_URL + quote_plus(paper_id) + f"?fields={fields}"

    try:
        resp = requests.get(url, headers=HEADERS, timeout=20)
        if resp.status_code != 200:
            return []
        data = resp.json()
        refs = data.get("references", [])
        time.sleep(SLEEP)
        return refs
    except:
        return []

def extract_arxiv_id(ext):
    """Extract arXiv ID from Semantic Scholar externalIds."""
    if not isinstance(ext, dict):
        return None
    for key in ["ArXiv", "arXiv", "ARXIV"]:
        if key in ext:
            return ext[key].replace("arXiv:", "").strip()
    return None

from sklearn.metrics.pairwise import cosine_similarity

def build_similarity_edges(doc_embs, doc_ids, top_k=3):
    """
    Builds edges based on semantic similarity.
    For each paper, connect it to its top_k most similar papers.
    """
    edges = []
    sims = cosine_similarity(doc_embs)

    for i in range(len(doc_ids)):
        # top-k most similar (skip itself)
        idxs = sims[i].argsort()[::-1][1:top_k+1]
        for j in idxs:
            edges.append((doc_ids[i], doc_ids[j]))

    return edges

def build_citation_graph(doc_ids):
    """
    Build citation graph using ONLY semantic similarity (Option C).
    """

    print("Building similarity-based citation graph...")

    # Load doc embeddings
    doc_embs = np.load(PROC/"doc_embs.npy")

    # Create directed graph
    G = nx.DiGraph()

    # Add nodes
    for pid in doc_ids:
        G.add_node(pid)

    # Build similarity edges
    sim_edges = build_similarity_edges(doc_embs, doc_ids, top_k=3)

    # Add edges
    for a, b in sim_edges:
        G.add_edge(a, b)

    print("Graph Complete → Nodes:", G.number_of_nodes(),
          "Edges:", G.number_of_edges())

    # compute centrality
    pagerank_scores = nx.pagerank(G)
    indegree = dict(G.in_degree())
    outdegree = dict(G.out_degree())

    return G, pagerank_scores, indegree, outdegree




