print("Loading PEGASUS abstractive model (google/pegasus-cnn_dailymail)...")
pegasus_model_name = "google/pegasus-cnn_dailymail"
pegasus_tokenizer = PegasusTokenizer.from_pretrained(pegasus_model_name)
pegasus_model = PegasusForConditionalGeneration.from_pretrained(
    pegasus_model_name,
    device_map="auto",
    low_cpu_mem_usage=True
)
device = pegasus_model.device

def abstractive_summarize_long_text(text, max_len=160, min_len=40, chunk_size=1024):
    """
    PEGASUS-based abstractive summarization for long docs:
      1) Split text into chunks of up to chunk_size characters
      2) Summarize each chunk
      3) Concatenate partial summaries
      4) Run a final PEGASUS pass over combined summary
    """
    text = clean_text(text)
    if not text:
        return ""

    # Rough character-based chunking
    chunks = [text[i:i+chunk_size] for i in range(0, len(text), chunk_size)]
    partial_summaries = []

    for chunk in chunks:
        inputs = pegasus_tokenizer(
            chunk,
            truncation=True,
            padding="longest",
            max_length=1024,
            return_tensors="pt"
        ).to(device)

        with torch.no_grad():
            summary_ids = pegasus_model.generate(
                **inputs,
                max_length=max_len,
                min_length=min_len,
                length_penalty=1.0,
                num_beams=4,
                early_stopping=True
            )

        summary_text = pegasus_tokenizer.decode(summary_ids[0], skip_special_tokens=True)
        partial_summaries.append(summary_text)

    combined = " ".join(partial_summaries)

    # Final fusion pass
    fusion_inputs = pegasus_tokenizer(
        combined,
        truncation=True,
        padding="longest",
        max_length=1024,
        return_tensors="pt"
    ).to(device)

    with torch.no_grad():
        fusion_ids = pegasus_model.generate(
            **fusion_inputs,
            max_length=max_len,
            min_length=min_len,
            length_penalty=1.0,
            num_beams=4,
            early_stopping=True
        )

    final_summary = pegasus_tokenizer.decode(fusion_ids[0], skip_special_tokens=True)
    return final_summary

