scorer = rouge_scorer.RougeScorer(['rouge1','rouge2','rougeL'], use_stemmer=True)

def evaluate_summary(reference, candidate):
    """
    Compute ROUGE-1/2/L and BERTScore-F1 between reference and candidate.
    Returns empty dict if reference too short.
    """
    reference = clean_text(reference)
    candidate = clean_text(candidate)

    if not reference or len(reference.split()) < 10:
        return {}

    rouge_scores = scorer.score(reference, candidate)
    # BERTScore can be slow, but more meaningful
    P, R, F1 = bert_score(
        [candidate],
        [reference],
        lang='en',
        rescale_with_baseline=True
    )

    return {
        "rouge1": float(rouge_scores['rouge1'].fmeasure),
        "rouge2": float(rouge_scores['rouge2'].fmeasure),
        "rougeL": float(rouge_scores['rougeL'].fmeasure),
        "bertscore_f1": float(F1[0])
    }

  # Information Retrieval Evaluation Metrics: Precision, Recall, F1

def compute_ir_metrics(query, retrieved_pids, meta_by_id):
    """
    Computes Precision, Recall, and F1-score for Information Retrieval.
    Ground truth relevance = title/abstract contains ANY query word.
    """

    q_words = query.lower().split()

    # ---- Step 1: Build Ground Truth Relevant Papers ----
    gt_relevant = []
    for pid, meta in meta_by_id.items():
        text = (meta.get("title", "") + " " + meta.get("abstract", "")).lower()
        if any(w in text for w in q_words):
            gt_relevant.append(pid)

    total_relevant = len(gt_relevant)
    if total_relevant == 0:
        return {
            "precision": 0,
            "recall": 0,
            "f1": 0,
            "relevant_found": 0,
            "total_relevant": 0
        }

    # Count Relevant Retrieved
    relevant_found = sum(1 for pid in retrieved_pids if pid in gt_relevant)

    precision = relevant_found / len(retrieved_pids)
    recall = relevant_found / total_relevant
    f1 = 2 * precision * recall / (precision + recall + 1e-9)

    return {
        "precision": precision,
        "recall": recall,
        "f1": f1,
        "relevant_found": relevant_found,
        "total_relevant": total_relevant
    }



# Full pipeline for a search query

def run_pipeline_for_query(query, max_results=20, embed_model_name="all-mpnet-base-v2"):
    """
    End-to-end:
      1) arXiv download (max_results)
      2) PDF -> text + metadata
      3) Build corpus
      4) Build indices (BM25 + dense + FAISS)

    Returns a 'state' dict reused for searches.
    """
    # 1) download from arXiv
    papers, raw_meta = download_papers_and_save_metadata(query, max_results=max_results, prefer_arxiv_abstract=True)

    # 2) extract texts + build processed metadata
    meta_list = extract_texts_and_build_meta(use_arxiv_meta=True)

    # 3) build corpus
    docs, doc_sentences, doc_ids = build_corpus_from_txt()
    if len(docs) == 0:
        raise RuntimeError("No documents found after preprocessing. Try increasing max_results or check PDF extraction.")

    # 4) build indices
    bm25, embed_model, doc_embs, index = build_indices(docs, embed_model_name=embed_model_name)

    # Persist basic doc index
    np.save(PROC/"doc_embs.npy", doc_embs)
    json.dump({"doc_ids": doc_ids}, open(PROC/"doc_index.json","w"))
    print("Pipeline finished: docs:", len(docs))

    # STEP 5: Build Citation Graph
    meta_by_id = {m['paper_id']: m for m in meta_list}
    G, pagerank_scores, indegree, outdegree = build_citation_graph(doc_ids)

    print("Pipeline finished: docs:", len(docs))

    return {
        "papers": papers,
        "meta_list": meta_list,
        "docs": docs,
        "doc_sentences": doc_sentences,
        "doc_ids": doc_ids,
        "bm25": bm25,
        "embed_model": embed_model,
        "doc_embs": doc_embs,
        "index": index,

        # Citation Graph Components
        "citation_graph": G,
        "pagerank": pagerank_scores,
        "indegree": indegree,
        "outdegree": outdegree,
    }



def pipeline_query_demo_from_state(state, query, method='dense', top_k=5):
    """
    Using a pre-built 'state', run retrieval + summarization + evaluation.

    For each retrieved paper, we compute:
      - extractive summary (BERTSUM-style)
      - abstractive summary (PEGASUS)
      - evaluation vs paper abstract for BOTH summaries
    """
    docs = state['docs']
    doc_sentences = state['doc_sentences']
    doc_ids = state['doc_ids']
    bm25 = state['bm25']
    embed_model = state['embed_model']
    index = state['index']
    meta_by_id = {m['paper_id']: m for m in state['meta_list']}

    # Retrieval
    if method == 'dense':
        retrieved = retrieve_dense_local(embed_model, index, doc_ids, query, top_k=top_k)
    else:
        retrieved = retrieve_bm25_local(bm25, doc_ids, query, top_k=top_k)

    #  Compute IR metrics
    retrieved_pids = [pid for pid, score in retrieved]
    ir_scores = compute_ir_metrics(query, retrieved_pids, meta_by_id)

    results = []

    for pid, score in retrieved:
        idx = doc_ids.index(pid)
        doc_text = docs[idx]
        sentences = doc_sentences[idx]
        meta = meta_by_id.get(pid, {})
        ref_abs = meta.get('abstract', "")

        # Extractive (BERTSUM)
        extractive_info = extractive_bertsum_summary(sentences, top_k=3)
        extractive_summary = " ".join([t[1] for t in extractive_info])

        # Abstractive (PEGASUS)
        abstractive_summary = abstractive_summarize_long_text(doc_text[:20000])

        # Evaluations (vs reference abstract)
        eval_extractive = evaluate_summary(ref_abs, extractive_summary) if ref_abs else {}
        eval_abstractive = evaluate_summary(ref_abs, abstractive_summary) if ref_abs else {}

        results.append({
            "paper_id": pid,
            "score": score,
            "title": meta.get('title', ""),
            "reference_abstract": ref_abs,
            "extractive_info": extractive_info,
            "extractive_summary": extractive_summary,
            "abstractive_summary": abstractive_summary,
            "eval_extractive": eval_extractive,
            "eval_abstractive": eval_abstractive
        })

    return {
        "ir_scores": ir_scores,
        "results": results
    }

