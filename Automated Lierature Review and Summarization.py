def clean_text(s):
    if s is None:
        return ""
    s = s.replace("\r"," ").replace("\n"," ")
    s = re.sub(r"\s+"," ", s).strip()
    return s


def sentence_split_spacy(text, min_chars=30):
    doc = nlp(text)
    sents = [sent.text.strip() for sent in doc.sents if len(sent.text.strip()) >= min_chars]

    if not sents:
        sents = [seg.strip() for seg in re.split(r'(?<=[\.\?\!])\s+', text) if len(seg.strip()) >= min_chars]
    return sents

