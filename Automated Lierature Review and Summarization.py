def plot_year_distribution(doc_ids):
    years = []
    for pid in doc_ids:
        y = get_year_from_arxiv_id(pid)
        if y:
            years.append(y)

    if not years:
        fig, ax = plt.subplots()
        ax.text(0.5, 0.5, "No year data available", ha="center")
        ax.axis("off")
        return fig

    year_counts = pd.Series(years).value_counts().sort_index()

    fig, ax = plt.subplots(figsize=(8, 5))
    ax.bar(year_counts.index.astype(str), year_counts.values,
           color="skyblue", edgecolor="black")

    ax.set_title("Year Distribution", fontsize=16, fontweight="bold")
    ax.set_xlabel("Year")
    ax.set_ylabel("Number of Papers")
    ax.tick_params(axis='x', rotation=45)

    plt.tight_layout()
    return fig

def visualize_citation_graph(G, pagerank_scores=None, max_nodes=40):
    if G.number_of_nodes() == 0:
        fig, ax = plt.subplots(figsize=(6, 4))
        ax.text(0.5, 0.5, "No citation graph available", ha="center")
        ax.axis("off")
        return fig

    # Select nodes
    if pagerank_scores:
        top_nodes = sorted(pagerank_scores.items(), key=lambda x: x[1], reverse=True)[:max_nodes]
        nodes_to_show = [n for n, _ in top_nodes]
    else:
        indeg = dict(G.in_degree())
        top_nodes = sorted(indeg.items(), key=lambda x: x[1], reverse=True)[:max_nodes]
        nodes_to_show = [n for n, _ in top_nodes]

    H = G.subgraph(nodes_to_show)

    # Node coloring
    if pagerank_scores:
        colors = [pagerank_scores[n] for n in H.nodes()]
    else:
        colors = "lightblue"

    fig = plt.figure(figsize=(10, 8))
    pos = nx.spring_layout(H, seed=42, k=0.4)

    nx.draw_networkx_nodes(
        H, pos, node_color=colors, cmap=plt.cm.plasma, node_size=500
    )
    nx.draw_networkx_edges(
        H, pos, arrowstyle="-|>", arrowsize=12, edge_color="gray"
    )
    nx.draw_networkx_labels(H, pos, font_size=8)

    plt.title("Citation Graph (Top Influential Papers)", fontsize=14)
    plt.axis("off")
    return fig


