bertsum_model = Summarizer()  # uses bert-base-uncased by default under the hood

def extractive_bertsum_summary(sentences, top_k=3):
    """
    Use BERT-based extractive summarizer.
    We join sentences into one text, let BERT pick important sentences,
    then map back to the original sentence list and return top_k in order.
    """
    if len(sentences) == 0:
        return []

    full_text = " ".join(sentences)

    summary_text = bertsum_model(full_text)

    # Split summary back to sentences and align by exact match / substring
    summary_sents = sentence_split_spacy(summary_text, min_chars=10)
    selected = []
    used_indices = set()

    for ss in summary_sents:
        for i, s in enumerate(sentences):
            if i in used_indices:
                continue
            # simple containment heuristic
            if ss.strip() in s or s in ss.strip():
                selected.append((i, s, 1.0))  # score is dummy here
                used_indices.add(i)
                break


    if len(selected) < top_k:
        for i, s in enumerate(sentences):
            if len(selected) >= top_k:
                break
            if i not in used_indices:
                selected.append((i, s, 0.5))

    # Sort by original order
    selected = sorted(selected, key=lambda x: x[0])
    return selected[:top_k]


