# Build arXiv query from free text or fielded terms
def build_arxiv_query_from_terms(raw_query):
    """
    Accepts free-form user query or fielded terms like:
    - "ai iot"
    - "cat:cs.CL"
    - "all:\"large language models\""
    Returns a combined arXiv query string.
    """
    terms = []
    for part in re.split(r'[,\n]+', raw_query.strip()):
        p = part.strip()
        if not p:
            continue
        # if user already gave fielded query, keep as is
        if re.match(r'^(cat:|ti:|au:|abs:|all:)', p, re.I):
            terms.append(p)
        else:
            # treat as keyword -> search in "all"
            if " " in p:
                terms.append(f'all:"{p}"')
            else:
                terms.append(f'all:{p}')
    return " OR ".join(terms) if terms else raw_query

# Search arXiv, download PDFs, and save basic metadata
def download_papers_and_save_metadata(query_string, max_results=30, prefer_arxiv_abstract=True):
    """
    1) Search arXiv using query_string.
    2) Download PDFs into RAW/.
    3) Save metadata (paper_id, title, abstract, authors) into PROC/metadata.json.

    Returns:
        papers (list[arxiv.Result])
        meta_list (list[dict])
    """
    q = build_arxiv_query_from_terms(query_string)
    print("Constructed arXiv query:", q)

    time.sleep(2)

    search = arxiv.Search(
        query=q,
        max_results=max_results,
        sort_by=arxiv.SortCriterion.SubmittedDate
    )
    papers = list(search.results())
    print("Found", len(papers), "results. Downloading PDFs (skipping cached)...")

    meta_list = []
    for p in tqdm(papers):

        time.sleep(1.2)

        arxiv_id = p.get_short_id()   # e.g., 2105.12345
        pdf_path = RAW / f"{arxiv_id}.pdf"
        if not pdf_path.exists():
            try:
                p.download_pdf(filename=str(pdf_path))
            except Exception as e:
                print("Download failed for", arxiv_id, "error:", e)
                continue

        title = p.title.strip() if getattr(p, "title", None) else ""
        summary = p.summary.strip() if getattr(p, "summary", None) else ""
        authors = [a.name for a in p.authors] if getattr(p, "authors", None) else []
        meta_list.append({
            "paper_id": arxiv_id,
            "title": title,
            "abstract": summary if prefer_arxiv_abstract else "",
            "authors": authors
        })

    # Save raw metadata
    json.dump(meta_list, open(PROC/"metadata.json", "w"), indent=2)
    print("Saved metadata for", len(meta_list), "papers to", PROC/"metadata.json")
    return papers, meta_list


def pdf_to_text(pdf_path: Path) -> str:
    """
    Reads full PDF and concatenates page text.
    """
    try:
        doc = fitz.open(str(pdf_path))
        pages = []
        for page in doc:
            pages.append(page.get_text("text"))
        return "\n".join(pages)
    except Exception as e:
        print("PDF parse error:", pdf_path, "error:", e)
        return ""

#  Build processed metadata
def extract_texts_and_build_meta(use_arxiv_meta=True):
    """
    For each PDF in RAW/:
      - Extract raw text -> save in TXT/{paper_id}.txt
      - Build improved metadata with:
            paper_id, title, abstract

    If arXiv metadata exists, we prefer:
      - arXiv title
      - arXiv abstract (if use_arxiv_meta=True)
    Otherwise we heuristically extract an abstract-like section
    from the PDF text.

    Saves:
      - PROC/metadata_processed.json

    Returns:
      meta_list (list[dict])
    """
    # Load arXiv metadata if present
    meta_by_arxiv = {}
    if (PROC/"metadata.json").exists():
        try:
            raw_meta = json.load(open(PROC/"metadata.json"))
            meta_by_arxiv = {m["paper_id"]: m for m in raw_meta}
        except Exception as e:
            print("Warning: could not read metadata.json:", e)

    meta_list = []
    pdf_files = sorted(RAW.glob("*.pdf"))
    print("Extracting text from", len(pdf_files), "PDFs...")

    for pdf in tqdm(pdf_files):
        pid = pdf.stem
        txt_path = TXT / f"{pid}.txt"

        # Extract and cache text if not present
        if not txt_path.exists():
            rawtxt = pdf_to_text(pdf)
            txt_path.write_text(rawtxt, encoding="utf-8")
        else:
            rawtxt = txt_path.read_text(encoding="utf-8")

        rawtxt_clean = clean_text(rawtxt)

        # Prefer arXiv title/abstract where available
        title = ""
        abstract = ""

        if pid in meta_by_arxiv:
            title = meta_by_arxiv[pid].get("title", "") or ""
            if use_arxiv_meta:
                abstract = meta_by_arxiv[pid].get("abstract", "") or ""

        # If no abstract from arXiv, try to extract from PDF text
        if not abstract:
            # Look for 'Abstract' section heuristically
            m = re.search(
                r'(?i)abstract[:\s]\s*(.+?)(?=\n(?:1\s+introduction|introduction|1\.\s+|keywords|1\s+))',
                rawtxt,
                re.S
            )
            if m:
                abstract = clean_text(m.group(1))

        # Final fallback: first ~800 chars of the text
        if not abstract:
            abstract = rawtxt_clean[:800]

        meta_list.append({
            "paper_id": pid,
            "title": title,
            "abstract": abstract
        })

    json.dump(meta_list, open(PROC/"metadata_processed.json", "w"), indent=2)
    print("Extraction complete. Papers processed:", len(meta_list))
    return meta_list

# Build corpus: doc texts, sentence lists, doc ids
def build_corpus_from_txt(min_sent_len=30, min_sents_per_doc=5):
    """
    Uses TXT/*.txt and PROC/metadata_processed.json to build:
        docs: list[str]          (full doc text as one string)
        doc_sentences: list[list[str]]  (each doc as list of sentences)
        doc_ids: list[str]       (paper ids)

    Abstract sentences are prepended (de-duplicated) for better summarization.
    """
    if (PROC/"metadata_processed.json").exists():
        meta_list = json.load(open(PROC/"metadata_processed.json"))
    else:
        meta_list = []
    meta_by_id = {d["paper_id"]: d for d in meta_list}

    docs = []
    doc_sentences = []
    doc_ids = []

    txt_files = sorted(TXT.glob("*.txt"))
    print("Building corpus from", len(txt_files), "TXT files...")

    for txt_file in tqdm(txt_files):
        pid = txt_file.stem
        raw = clean_text(txt_file.read_text(encoding="utf-8"))
        sents = sentence_split_spacy(raw, min_chars=min_sent_len)

        # Prepend abstract sentences if available
        if pid in meta_by_id and meta_by_id[pid].get("abstract"):
            abs_sents = sentence_split_spacy(meta_by_id[pid]["abstract"], min_chars=20)
            combined = []
            for s in abs_sents + sents:
                if s not in combined:
                    combined.append(s)
            sents = combined

        if len(sents) < min_sents_per_doc:
            continue

        docs.append(" ".join(sents))
        doc_sentences.append(sents)
        doc_ids.append(pid)

    print("Loaded docs:", len(docs))
    pd.DataFrame({"paper_id": doc_ids}).to_csv(PROC/"doc_ids.csv", index=False)
    return docs, doc_sentences, doc_ids

# Retrieval indices (BM25 + dense embeddings + FAISS)

def build_indices(docs, embed_model_name="all-mpnet-base-v2", batch_size=16):
    """
    Builds:
      - BM25 index over tokenized docs
      - Dense embeddings using SentenceTransformer
      - FAISS index over normalized embeddings (cosine similarity via inner product)
    """
    print("Building BM25 index...")
    tokenized_for_bm25 = [doc.split() for doc in docs]
    bm25 = BM25Okapi(tokenized_for_bm25)
    print("BM25 index built.")

    print(f"Loading SentenceTransformer embedding model: {embed_model_name}")
    embed_model = SentenceTransformer(embed_model_name)
    device = "cuda" if torch.cuda.is_available() else "cpu"
    embed_model = embed_model.to(device)
    print("Encoding documents...")
    doc_embs = embed_model.encode(
        docs,
        convert_to_numpy=True,
        show_progress_bar=True,
        batch_size=batch_size
    )

    # FAISS index for cosine similarity (normalize first, then use inner product)
    faiss.normalize_L2(doc_embs)
    d = doc_embs.shape[1]
    index = faiss.IndexFlatIP(d)
    index.add(doc_embs)
    print("FAISS index built. Vectors:", index.ntotal)
    return bm25, embed_model, doc_embs, index

# --- Retrieval helpers (BM25 + dense) ---

def retrieve_bm25_local(bm25, doc_ids, query, top_k=5):
    tokenized_q = query.split()
    scores = bm25.get_scores(tokenized_q)
    idxs = np.argsort(scores)[::-1][:top_k]
    return [(doc_ids[i], float(scores[i])) for i in idxs]

def retrieve_dense_local(embed_model, index, doc_ids, query, top_k=5):
    device = "cuda" if torch.cuda.is_available() else "cpu"
    q_emb = embed_model.encode([query], convert_to_numpy=True, device=device)
    faiss.normalize_L2(q_emb)
    D, I = index.search(q_emb, top_k)
    results = []
    for j, i in enumerate(I[0]):
        results.append((doc_ids[int(i)], float(D[0][j])))
    return results

